def highest_gc(string):
  # splita a lista
  tmp = string.split(">")
  tmp = string.split("\n")
  
  # loop para separar labels de dados
  labels = {}
  label = ''
  for i in tmp:
    if i.startswith('>'):
      label = i[1:]
      labels[label] = ''
    else:
      labels[label] = labels[label] + i
      
  # faz as contagens
  max_gc, count = 0.0, 0.0
  label = ''
  for key, value in labels.items():
    count = 0
    for item in value:
      if item == 'G' or item == 'C':
        count = count + 1.0
    if float(count*100/len(value)) > max_gc:
      label = key
      max_gc = float(count*100/len(value))
  return label, max_gc

